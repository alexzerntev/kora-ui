/**
 * Process run instances — represents actual executions of workflow definitions.
 * Each run references a workflow (process) and tracks its runtime status.
 */

export type RunStatus = 'running' | 'completed' | 'failed' | 'paused'

export type NodeRunState = 'done' | 'running' | 'idle' | 'skipped'

export interface ProcessRun {
  id: string
  workflowId: string
  workflowName: string
  status: RunStatus
  startedAt: string
  completedAt?: string
  progress: number // 0-100
  currentStep: string
  stepsCompleted: number
  stepsTotal: number
  triggeredBy: string
  nodeStates?: Record<string, NodeRunState>
}

