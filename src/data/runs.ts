/**
 * Pending actions — tasks waiting for human input
 */

export type PendingActionType = 'approval' | 'review' | 'decision' | 'input'

export interface PendingAction {
  id: string
  title: string
  description: string
  type: PendingActionType
  assigneeId: string
  assigneeName: string
  processRunId: string
  processName: string
  priority: 'high' | 'medium' | 'low'
  createdAt: string
  dueAt?: string
}

